public class ArrayContainer {

    private int[] arr1;
    private int[] arr2;

    public ArrayContainer(int[] arr1, int[] arr2) {
        this.arr1 = arr1;
        this.arr2 = arr2;
    }

    public void setArr1(int[] arr1) {
        this.arr1 = arr1;
    }

    public void setArr2(int[] arr2) {
        this.arr2 = arr2;
    }

    public int[] getArr1() {
        return arr1;
    }

    public int[] getArr2() {
        return arr2;
    }
}
